const averageOfThreeNumbers = (a, b, c) => {
  return (a + b + c) / 3;
};

// Example usage:
console.log(averageOfThreeNumbers(5, 10, 15)); // Output:
// Output: 10
console.log(averageOfThreeNumbers(-2, 4, 6)); // Output:
// Output: 2.6666666666666665
console.log(averageOfThreeNumbers(0.5, 10, 20)); // Output:
// Output: 10.166666666666666
