/*
### 14. Array Destructuring
Given an array numbers containing the numbers 1, 2, and 3. Use array destructuring to log each number to the console.
```js
const numbers = [1, 2, 3]
console.log(a) // 1
console.log(b) // 2
console.log(c) // 3
```
 */

const numbers = [1, 2, 3];
const [a, b, c] = numbers;
console.log(a); // 1
console.log(b); // 2
console.log(c); // 3
